package com.thanhtuan.posnet.model;

public class ThongTinGiaoHang {
    private Boolean NoiGiao;
    private String  DiaChi;
    private String  NgayGiao;
    private String  ThoiGian;
    private String  Quay;
    private String  LayHang;
    private String  GiaoHang;

    public ThongTinGiaoHang() {
    }

    public Boolean getNoiGiao() {
        return NoiGiao;
    }

    public void setNoiGiao(Boolean noiGiao) {
        NoiGiao = noiGiao;
    }

    public String getDiaChi() {
        return DiaChi;
    }

    public void setDiaChi(String diaChi) {
        DiaChi = diaChi;
    }

    public String getNgayGiao() {
        return NgayGiao;
    }

    public void setNgayGiao(String ngayGiao) {
        NgayGiao = ngayGiao;
    }

    public String getThoiGian() {
        return ThoiGian;
    }

    public void setThoiGian(String thoiGian) {
        ThoiGian = thoiGian;
    }

    public String getQuay() {
        return Quay;
    }

    public void setQuay(String quay) {
        Quay = quay;
    }

    public String getLayHang() {
        return LayHang;
    }

    public void setLayHang(String layHang) {
        LayHang = layHang;
    }

    public String getGiaoHang() {
        return GiaoHang;
    }

    public void setGiaoHang(String giaoHang) {
        GiaoHang = giaoHang;
    }
}
